def _slug(s: str) -> str:
    s = s.strip().lower().replace(" ", "_")
    out = []
    prev_us = False
    for ch in s:
        ok = ("a" <= ch <= "z") or ("0" <= ch <= "9")
        if ok:
            out.append(ch)
            prev_us = False
        else:
            if not prev_us:
                out.append("_")
                prev_us = True
    slug = "".join(out).strip("_")
    while "__" in slug:
        slug = slug.replace("__", "_")
    return slug or "partner"


def prompt(label: str, hint: str) -> str:
    while True:
        try:
            s = input(f"  {label} ({hint}): ").strip()
        except EOFError:
            sys.exit("\naborted")
        if s:
            return s
        print("    ↳ value required")


def prompt_yn(label: str, default: bool = False) -> bool:
    suffix = "[y/N]" if not default else "[Y/n]"
    while True:
        try:
            s = input(f"  {label} {suffix}: ").strip().lower()
        except EOFError:
            sys.exit("\naborted")
        if not s:
            return default
        if s in ("y", "yes"):
            return True
        if s in ("n", "no"):
            return False
        print("    ↳ please answer y or n")


def find_typst() -> str:
    path = shutil.which("typst")
    if path:
        return path
    sys.exit(
        "error: `typst` binary not found on PATH.\n"
        "install it from https://github.com/typst/typst/releases — for example:\n"
        "  macOS:    brew install typst\n"
        "  Linux:    cargo install --locked typst-cli\n"
        "  Windows:  winget install --id Typst.Typst"
    )


def _english_list(items: list[str], article: str = "the") -> str:
    if not items:
        return "no designated areas"
    prefixed = [f"{article} {item}" for item in items]
    if len(prefixed) == 1:
        return prefixed[0]
    if len(prefixed) == 2:
        return f"{prefixed[0]} and {prefixed[1]}"
    return ", ".join(prefixed[:-1]) + f", and {prefixed[-1]}"


